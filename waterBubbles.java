
public class waterBubbles {
	private double x;
	 private double y;
	 private double yvelocity;
	 private double size;
	public waterBubbles(double x, double y, double yvelocity, double size) {
		
		this.x = x;
		this.y = y;
		this.yvelocity = yvelocity;
		this.size = size;
	}
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
	public double getYvelocity() {
		return yvelocity;
	}
	public void setYvelocity(double yvelocity) {
		this.yvelocity = yvelocity;
	}
	public double getSize() {
		return size;
	}
	public void setSize(double size) {
		this.size = size;
	}
	 
}
